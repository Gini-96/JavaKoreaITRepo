package personer;

public class ExPrimitiveTypeCasting {

	public static void main(String[] args) {
		
		byte b = 1;
		short sb = b;
		int ib = b;
		long lb = b;
		float fb = b;
		double db = b;
		char cb = (char)b;
		
		short s = 2;
		byte bs = (byte)s;
		int is = s;
		long ls = s;
		float fs = s;
		double ds = s;
		char cs = (char)s;
		
		int i = 3;
		byte bi = (byte)i;
		short si = (short)i;
		long li = i;
		float fi = i;
		double di = i;
		char ci = (char)i;
		
		long l = 4;
		byte bl = (byte)l;
		short sl = (short)l;
		int il = (int)l;
		float fl = l;
		double dl = l;
		char cl = (char)l;
		
		float f = 1.0f;
		byte bf = (byte)f;
		short sf = (short)f;
		int inf = (int)f;
		long lf = (long)f;
		double df = f;
		char cf = (char)f;
		
		double d = 2.0;
		byte bd = (byte)d;
		short sd = (short)d;
		int id = (int)d;
		long ld = (long)d;
		float fd = (float)d;
		char cd = (char)d;
		
		char c = 'A';
		byte bc = (byte)c;
		short sc = (short)c;
		int ic = c;
		long lc = c;
		float fc = c;
		double dc = c;
		
		System.out.println();
		
	}
	
	
}
